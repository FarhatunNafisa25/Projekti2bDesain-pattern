package modul3.latihan.bagian4.overloading;

class Matematika {
    // Method tambah() dengan 2 parameter bertipe int
    public int tambah(int a, int b) {
        return a + b;
    }

    // Method tambah() dengan 3 parameter bertipe int
    public int tambah(int a, int b, int c) {
        return a + b + c;
    }

    // Method tambah() dengan 2 parameter bertipe double
    public double tambah(double a, double b) {
        return a + b;
    }
}
